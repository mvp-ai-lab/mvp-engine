def _example_loss_threshold_degraded() -> GoalSpec:
    """Scenario 3 (degraded): "loss 一直不降" — v1 近似为阈值监控。

    真正的 trend detection 需要 window/trend rule type，留 v2。
    v1 compiler 将其翻译为 "loss >= 5.0 连续 10 次就报警"。

    Goal: "loss 一直不降就提醒我"
    """

    return GoalSpec(
        intent_id=_generate_intent_id(),
        goal_text="loss 一直不降就提醒我",
        workflow=WorkflowPlan(
            mode="train",
            recipe="openbee",
            stage="stage1",
        ),
        monitors=[
            MonitorSpec(
                name="loss_plateau_approx",
                event_type="progress_updated",
                field="metrics.loss",
                operator=">=",
                target=5.0,
                description="v1 degraded: approximate plateau detection via threshold",
                trigger_mode="consecutive",
                trigger_count=10,
            ),
        ],
        metadata={"compiler": "llm_v1", "degraded_scenarios": ["trend_detection"]},
    )


